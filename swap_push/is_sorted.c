#include "push_swap.h"

int	is_sorted(t_stack *stack_a)
{
	t_node	*current;

	if (!stack_a || stack_a->size < 2)
		return (1);
	current = stack_a->top;
	while (current && current->next)
	{
		if (current->value > current->next->value)
			return (0);
		current = current->next;
	}
	return (1);
}
